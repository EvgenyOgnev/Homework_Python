# 4.1. Вычислить число c заданной точностью d
# Пример: - при d = 0.001, π = 3.141
#  Ввод: 0.01
#  Вывод: 3.14

# Ввод: 0.001
# Вывод: 3.141

import math

pi = math.pi
print('пи = ', pi)
d = input('Введите точность: ')
pi_decimal = (str(d)).split('.')[1]
print(f'При точности {d}, пи = {round(pi, len(pi_decimal))}')
